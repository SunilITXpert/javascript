// Operators - JavaScript supports various types of operators that allow you to perform operations on variables and values.
// Arithmetic Operators: 
// Addition (+):


let sum = 10 + 5; // sum = 15

// Subtraction (-):
let difference = 10 - 5; // difference = 5

//Multiplication (*):

let product = 10 * 5; // product = 50

//Division (/):
let quotient = 10 / 5; // quotient = 2

// Remainder (Modulus) (%):

let remainder = 10 % 3; // remainder = 1 (10 divided by 3 gives remainder 1)

// Comparison Operators: These operators compare two values and return a Boolean result (true or false).

// Equal to (==):
console.log(10 == 10); // Output: true

// Not equal to (!=):

console.log(10 != 5); // Output: true

// Strict equal to (===):

console.log(10 === "10"); // Output: false (compares both value and type)

// Strict not equal to (!==):

console.log(10 !== "10"); // Output: true (compares both value and type)

// Greater than (>), Greater than or equal to (>=):

console.log(10 > 5); // Output: true
console.log(10 >= 10); // Output: true

// Less than (<), Less than or equal to (<=):

console.log(10 < 5); // Output: false
console.log(10 <= 10); // Output: true

// Logical Operators: These operators are typically used with Boolean (logical) values and return a Boolean result.

// Logical AND (&&):
console.log(true && false); // Output: false

// Logical OR (||):

console.log(true || false); // Output: true

// Logical NOT (!): 

console.log(!true); // Output: false

// Assignment Operators: These operators assign values to variables.
let x = 10;
// Add and assign (+=), Subtract and assign (-=), etc.:

let y = 5;
y += 3; // y = y + 3

// Unary Operators: Operate on a single operand.

// Increment (++):

let a = 5;
a++; // a = a + 1

// Decrement (--):

let b = 5;
b--; // b = b - 1

// String Operators: Concatenate strings. 

// Concatenation (+)

let greeting = "Hello";
let name = "Alice";
let message = greeting + ", " + name + "!"; // message = "Hello, Alice!"


